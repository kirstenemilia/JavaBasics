public class HelloWorld
{
    public static void main(String [] args)
    {
        System.out.println("Hello, World!"); // Prints Hello, World! to console
        
        System.out.println("Name: Kirsten Santos"); // Prints name to console

        System.out.println("Favorite food: Sashimi"); // Prints favorite food to console

        System.out.println("Person I'd like to meet: Amelia Earhart"); // Prints person I'd like to meet to console

        System.out.println("Least favorite chore: Folding laundry"); // Prints least favorite chore to console

        System.out.println("What I'd rather be doing right now: Reading my book"); // Prints what I'd rather be doing right now to console
    }
}

/* 
 * The main method represents the entry point for execution in a Java program. 
 * The Java Virtual Machine loads the class containing the main method into memory and then calls it to start the program's execution.
 */

